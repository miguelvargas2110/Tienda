    void buscarCliente(){
        int r;
        String id = txtIdCliente.getText();
        if(txtIdCliente.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe ingresar un codigo de Cliente");
        }else{
            Cliente cliente = new ClientesGUI().listarID(id);
            if(cliente.getId()!=null){
                txtCliente.setText(cliente.getNombre());
                txtIdProducto.requestFocus();
            }else{
                r=JOptionPane.showConfirmDialog(this, "El cliente aun no esta registrado. Â¿Desea registrarlo?");
                if(r==0){
                    ClientesGUI cg = new ClientesGUI();
                    cg.setVisible(true);
                }
            }
        }
    }
